"""# Rerun Based on Our Own Engagement Metric

To create a true representation of engagement, we can combine three specific columns: Clicks, Impressions, and Engagement_Score.

Click-Through Rate (CTR): We calculate Clicks / Impressions to see what percentage of people actually stopped scrolling and clicked.

Quality: We multiply that percentage by the 1-10 Engagement_Score. A high CTR with a low Engagement Score (e.g., clickbait) will be penalized, while a decent CTR with a perfect 10 Engagement Score will be heavily rewarded.

We divide the whole thing by Acquisition_Cost to see how many "Engagement Points" the company gets for every $1 spent.
"""

import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from fitter import Fitter
from ortools.linear_solver import pywraplp
import scipy.stats as stats
