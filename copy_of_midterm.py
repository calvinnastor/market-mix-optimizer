# Math: (CTR * Engagement Score * 1000) / Acquisition_Cost
df['True_Engagement_Points'] = (df['Clicks'] / df['Impressions']) * df['Engagement_Score'] * 1000
df['Engagement_Per_Dollar'] = df['True_Engagement_Points'] / df['Acquisition_Cost']

campaigns = df['Campaign_Type'].unique().tolist()
results = {}

print("Fitting distributions for Custom Engagement Metric by Campaign Type...")
